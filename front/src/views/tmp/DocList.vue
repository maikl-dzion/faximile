export default {
  data: () => ({
    docList : [],
    dialog: false,
    headers: [
      {
        text: "ДОКУМЕНТ",
        align: "start",
        sortable: false,
        value: "name"
      },
      { text: "КОМПАНИЯ"   , value: "calories" },
      { text: "СОТРУДНИК"  , value: "fat" },
      { text: "СТАТУС"     , value: "carbs" },

      { text: "Protein (g)", value: "protein" },
      { text: "Actions"    , value: "actions", sortable: false }
    ],

    desserts: [],
    editedIndex: -1,
    editedItem: {
      name: "",
      calories: 0,
      fat: 0,
      carbs: 0,
      protein: 0
    },

    defaultItem: {
      name: "",
      calories: 0,
      fat: 0,
      carbs: 0,
      protein: 0
    }
  }),

  computed: {
    formTitle() {
      return this.editedIndex === -1 ? "New Item" : "Edit Item";
    }
  },

  watch: {
    dialog(val) {
      val || this.close();
    }
  },

  created() {
    this.getList();
    this.initialize();
  },

  methods: {

     getList() {
         const url = 'doc_list';
         this.makeRequest(url).then(response => {
           this.docList = response.data
         })
     },

    initialize() {

      this.desserts = [

        {
          name: "Frozen Yogurt",
          calories: 159,
          fat: 6.0,
          carbs: 24,
          protein: 4.0
        },
        {
          name: "Ice cream sandwich",
          calories: 237,
          fat: 9.0,
          carbs: 37,
          protein: 4.3
        },
      
      ];
    },

    editItem(item) {
      this.editedIndex = this.desserts.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialog = true;
    },

    deleteItem(item) {
      const index = this.desserts.indexOf(item);
      confirm("Are you sure you want to delete this item?") &&
        this.desserts.splice(index, 1);
    },

    close() {
      this.dialog = false;
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      });
    },

    save() {
      if (this.editedIndex > -1) {
        Object.assign(this.desserts[this.editedIndex], this.editedItem);
      } else {
        this.desserts.push(this.editedItem);
      }
      this.close();
    }

  }
};
